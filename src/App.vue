import LayoutComponent from './components/LayoutComponent.vue'

export default {
  name: 'app',
  components: {
    LayoutComponent
  },
  methods: {
    addClass: function(id, cls){
      var x = document.getElementsByClassName(id);
      for (var i = 0; i < x.length; i++) {
        if(x[i].classList.contains(cls) == false) x[i].classList.add(cls);
      }
    },
    removeClass: function(id, cls){

      var x = document.getElementsByClassName(id);
      for (var i = 0; i < x.length; i++) {
          if(x[i].classList.contains(cls) == true) x[i].classList.remove(cls);
      }
    },
    hideModal: function(){
      var lens = document.getElementById("modal");
      lens.style.display = "none";
    },
    showView: function (p,id) {
      var hideclass = "hidden";

      this.addClass(p + "-t", hideclass);
      this.addClass(p + "-f", hideclass);
      this.addClass(p + "-l", hideclass);
      this.addClass(p + "-r", hideclass);
      this.addClass(p + "-2h", hideclass);

      this.removeClass(p + "-" +id, hideclass);
      
    }
  }
}
